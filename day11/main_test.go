func TestSolvePuzzle1(t *testing.T) {
	input := `
L.LL.LL.LL
LLLLLLL.LL
L.L.L..L..
LLLL.LL.LL
L.LL.LL.LL
L.LLLLL.LL
..L.L.....
LLLLLLLLLL
L.LLLLLL.L
L.LLLLL.LL
`

	seats, err := readInput(strings.NewReader(input))
	if err != nil {
		t.Fatal(err)
	}

	answer := solvePuzzle1(seats)
	if answer != 37 {
		t.Fatalf("expected answer 37, got %d", answer)
	}
}

